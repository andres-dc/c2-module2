require './red_color_detector'

arr1 = [
  [[65, 67, 23], [234, 176, 0], [143, 0, 0]],
  [[255, 30, 51], [156, 41, 38], [3, 243, 176]],
  [[255, 255, 255], [0, 0, 0], [133, 28, 13]],
  [[26, 43, 255], [48, 2, 2], [57, 89, 202]],
]
arr2 = [
  [[165, 67, 23], [234, 6, 0], [143, 0, 0]],
  [[255, 30, 51], [254, 41, 38], [253, 243, 176]],
  [[255, 255, 255], [0, 0, 0], [233, 28, 13]],
  [[26, 43, 255], [8, 90, 156], [57, 89, 202]],
]
arr3 = [
  [[165, 7, 223], [23, 16, 20], [143, 56, 0]],
  [[25, 93, 251], [26, 241, 38], [53, 23, 176]],
  [[0, 255, 255], [0, 222, 0], [133, 47, 213]],
  [[126, 143, 55], [248, 90, 142], [17, 9, 22]],
]

describe "#red_color_detector" do
  it "returns the number of red pixels on an array" do
    expect(red_color_detector(arr1)).to eql(3)
    expect(red_color_detector(arr2)).to eql(5)
    expect(red_color_detector(arr3)).to eql(0)
  end
end
